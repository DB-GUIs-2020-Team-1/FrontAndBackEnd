export class User {
    constructor(userID, email, username, password, grade, school, major, gradDate) {
        this.userID = userID;
        this.email = email;
        this.username = username;
        this.password = password;
        this.grade = grade;
        this.school = school;
        this.major = major;
        this.gradDate = gradDate;
    }
}